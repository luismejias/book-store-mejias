export const  getDataBooks = () => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      if (bookList.length > 0) {
        resolve(bookList);
      } else {
        reject('ocurrio un error');
      }
    }, 1000);
  });
}
export const  getDataBooksDetail = (id) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      if (bookList.length > 0) {
        const bookFilter = 
          bookList.find((book)=>{
            return book.code === id;
          })
        
        resolve(bookFilter);
      } else {
        reject('ocurrio un error');
      }
    }, 1000);
  });
}